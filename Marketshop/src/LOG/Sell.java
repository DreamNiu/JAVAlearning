package LOG;

public class Sell {
	private String S_ID;
	private String S_NAME;
	private Double S_SELLPRICE;
	private Double S_NUM;
	private Double S_TOTAL;
	public Sell(){
		
	}
	public String getS_ID() {
		return S_ID;
	}
	public void setS_ID(String s_ID) {
		S_ID = s_ID;
	}
	public String getS_NAME() {
		return S_NAME;
	}
	public void setS_NAME(String s_NAME) {
		S_NAME = s_NAME;
	}
	public Double getS_SELLPRICE() {
		return S_SELLPRICE;
	}
	public void setS_SELLPRICE(Double s_SELLPRICE) {
		S_SELLPRICE = s_SELLPRICE;
	}
	public Double getS_NUM() {
		return S_NUM;
	}
	public void setS_NUM(Double s_NUM) {
		S_NUM = s_NUM;
	}
	public Double getS_TOTAL() {
		return S_TOTAL;
	}
	public void setS_TOTAL(Double s_TOTAL) {
		S_TOTAL = s_TOTAL;
	}
	
	

}
