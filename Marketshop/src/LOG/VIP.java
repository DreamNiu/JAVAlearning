package LOG;



public class VIP {
	private String V_ID;
	private String V_NAME;
	private String V_TEL;
	private String V_ADRESS;
	private String V_DATE;
	private String V_PERIOD;
	private String V_EMAIL;
	public String getV_ID() {
		return V_ID;
	}
	public void setV_ID(String v_ID) {
		V_ID = v_ID;
	}
	public String getV_NAME() {
		return V_NAME;
	}
	public void setV_NAME(String v_NAME) {
		V_NAME = v_NAME;
	}
	public String getV_TEL() {
		return V_TEL;
	}
	public void setV_TEL(String v_TEL) {
		V_TEL = v_TEL;
	}
	public String getV_ADRESS() {
		return V_ADRESS;
	}
	public void setV_ADRESS(String v_ADRESS) {
		V_ADRESS = v_ADRESS;
	}
	public String getV_DATE() {
		return V_DATE;
	}
	public void setV_DATE(String v_DATE) {
		V_DATE = v_DATE;
	}
	public String getV_PERIOD() {
		return V_PERIOD;
	}
	public void setV_PERIOD(String v_PERIOD) {
		V_PERIOD = v_PERIOD;
	}
	public String getV_EMAIL() {
		return V_EMAIL;
	}
	public void setV_EMAIL(String v_EMAIL) {
		V_EMAIL = v_EMAIL;
	}

	

}
